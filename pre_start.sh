#!/usr/bin/env bash
# ============================================================
#  Pre-start cleanup для host-manager.service.
#  Запускается systemd через ExecStartPre перед стартом панели.
#
#  Если кто-то «сирота» (старая, не убитая по SIGTERM версия панели,
#  или унаследовавший fd сокета tmux-клиент) держит наш порт —
#  убиваем его, чтобы новый процесс мог начать слушать порт чисто.
#
#  Это делает сервис самолечащимся: даже если когда-то процесс
#  не умер по SIGTERM, на следующем `systemctl restart` он будет
#  принудительно убит, и панель поднимется на новом коде.
# ============================================================
set +e

P="${MANAGER_PORT:-80}"

# Все процессы, занимающие наш порт
pids=$(ss -tlnHp "sport = :${P}" 2>/dev/null \
       | grep -oP 'pid=\K[0-9]+' | sort -u)

[ -z "$pids" ] && exit 0

for pid in $pids; do
    # Командная строка процесса
    cmd=$(tr -d '\0' </proc/$pid/cmdline 2>/dev/null)
    case "$cmd" in
        # Убиваем только своих: процессы панели (manager.py) и
        # tmux-клиенты, унаследовавшие сокет от старой панели.
        # Посторонние сервисы (nginx, apache и т.п.) — НЕ ТРОГАЕМ.
        *manager.py*|*tmux*)
            kill -9 "$pid" 2>/dev/null
            ;;
    esac
done

# Небольшая пауза, чтобы ядро успело освободить сокеты
sleep 0.3
exit 0
